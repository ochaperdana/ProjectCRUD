    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris=jTable1.getSelectedRow();
        if (baris!=-1) {
            tfNIS.setText(jTable1.getValueAt(baris, 0).toString());
            tfNama.setText(jTable1.getValueAt(baris, 1).toString());
            if ("Laki-laki".equals(jTable1.getValueAt(baris, 2).toString())){
                rbLaki.setSelected(true);
            }
            else{
                rbPerempuan.setSelected(true);
            }
            tfKelas.setText(jTable1.getValueAt(baris, 3).toString());
            tfEmail.setText(jTable1.getValueAt(baris, 4).toString());
            taAlamat.setText(jTable1.getValueAt(baris, 5).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked

    public void selectData(){
        String kolom[]={"NIS","NamaSiswa","JenisKelamin","Kelas","Email","Alamat"};
        DefaultTableModel dtm=new DefaultTableModel(null, kolom);
        String SQL="SELECT * FROM t_siswa";
        ResultSet rs=KoneksiDB.executeQuery(SQL);
        try {
            while(rs.next()){
                String NIS=rs.getString(1);
                String NamaSiswa=rs.getString(2);
                String JenisKelamin="";
                if ("L".equals(rs.getString(3))){
                    {
                    JenisKelamin="Laki-laki";
                    }}
                else{
                        JenisKelamin="Perempuan";
                            }
                String Kelas=rs.getString(4);
                String Email=rs.getString(5);
                String Alamat=rs.getString(6);
                String data[]={NIS,NamaSiswa,JenisKelamin,Kelas,Email,Alamat};
                dtm.addRow(data);
            }
        } catch (SQLException ex) {
        //Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTable1.setModel(dtm);
    }
